"""
src/ingestion/data_loader.py
=============================
Loads transaction data from any supported source and normalises it
to the canonical schema used throughout the pipeline:

    sender_account | receiver_account | transaction_amount | timestamp | is_fraud

Supported sources:
  - PaySim CSV  (your existing dataset)
  - AMLSim CSV
  - Synthetic   (generated by data_generator.py)
  - Any CSV that can be column-mapped
"""

